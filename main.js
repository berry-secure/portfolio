const canvas = document.getElementById("signal-canvas");
const ctx = canvas.getContext("2d");
const reducedMotion = window.matchMedia("(prefers-reduced-motion: reduce)").matches;

const palette = {
  teal: "#20a4a8",
  coral: "#e85d4f",
  gold: "#f2b84b",
  violet: "#b35dc6",
  white: "#fffdf8",
};

const nodes = [
  { x: 0.12, y: 0.28, r: 4, c: palette.teal },
  { x: 0.28, y: 0.44, r: 3, c: palette.gold },
  { x: 0.48, y: 0.22, r: 5, c: palette.coral },
  { x: 0.67, y: 0.5, r: 3, c: palette.violet },
  { x: 0.84, y: 0.31, r: 4, c: palette.teal },
  { x: 0.74, y: 0.72, r: 4, c: palette.gold },
  { x: 0.36, y: 0.78, r: 3, c: palette.coral },
  { x: 0.16, y: 0.66, r: 4, c: palette.violet },
];

function resizeCanvas() {
  const ratio = Math.min(window.devicePixelRatio || 1, 2);
  const rect = canvas.getBoundingClientRect();
  canvas.width = Math.max(1, Math.floor(rect.width * ratio));
  canvas.height = Math.max(1, Math.floor(rect.height * ratio));
  ctx.setTransform(ratio, 0, 0, ratio, 0, 0);
}

function drawFrame(time) {
  const width = canvas.clientWidth;
  const height = canvas.clientHeight;
  ctx.clearRect(0, 0, width, height);
  ctx.fillStyle = "#101218";
  ctx.fillRect(0, 0, width, height);

  ctx.strokeStyle = "rgba(255,253,248,0.075)";
  ctx.lineWidth = 1;
  const grid = 56;
  for (let x = 0; x < width; x += grid) {
    ctx.beginPath();
    ctx.moveTo(x, 0);
    ctx.lineTo(x, height);
    ctx.stroke();
  }
  for (let y = 0; y < height; y += grid) {
    ctx.beginPath();
    ctx.moveTo(0, y);
    ctx.lineTo(width, y);
    ctx.stroke();
  }

  const t = time * 0.00018;
  const points = nodes.map((node, index) => ({
    x: node.x * width + Math.sin(t * 5 + index) * 18,
    y: node.y * height + Math.cos(t * 4 + index * 0.8) * 14,
    r: node.r,
    c: node.c,
  }));

  ctx.lineWidth = 1.4;
  points.forEach((point, index) => {
    const next = points[(index + 1) % points.length];
    const nextTwo = points[(index + 3) % points.length];
    drawLink(point, next, index);
    drawLink(point, nextTwo, index + 3, 0.42);
  });

  points.forEach((point, index) => {
    ctx.beginPath();
    ctx.arc(point.x, point.y, point.r + 8, 0, Math.PI * 2);
    ctx.fillStyle = point.c + "24";
    ctx.fill();
    ctx.beginPath();
    ctx.arc(point.x, point.y, point.r, 0, Math.PI * 2);
    ctx.fillStyle = point.c;
    ctx.fill();
    if (index % 2 === 0) {
      ctx.fillStyle = "rgba(255,253,248,0.72)";
      ctx.font = "700 12px Inter, system-ui, sans-serif";
      ctx.fillText(["prompt", "context", "eval", "ship"][index / 2], point.x + 14, point.y + 4);
    }
  });

  drawPackets(points, time);

  if (!reducedMotion) requestAnimationFrame(drawFrame);
}

function drawLink(a, b, index, alpha = 0.72) {
  ctx.beginPath();
  ctx.moveTo(a.x, a.y);
  const cx = (a.x + b.x) / 2 + Math.sin(index) * 34;
  const cy = (a.y + b.y) / 2 + Math.cos(index) * 30;
  ctx.quadraticCurveTo(cx, cy, b.x, b.y);
  ctx.strokeStyle = `rgba(255,253,248,${alpha * 0.28})`;
  ctx.stroke();
}

function drawPackets(points, time) {
  for (let index = 0; index < points.length; index += 1) {
    const a = points[index];
    const b = points[(index + 1) % points.length];
    const phase = (time * 0.00016 + index / points.length) % 1;
    const x = a.x + (b.x - a.x) * phase;
    const y = a.y + (b.y - a.y) * phase;
    ctx.fillStyle = index % 3 === 0 ? palette.gold : index % 3 === 1 ? palette.teal : palette.coral;
    ctx.fillRect(x - 3, y - 3, 6, 6);
  }
}

window.addEventListener("resize", () => {
  resizeCanvas();
  drawFrame(performance.now());
});

resizeCanvas();
drawFrame(performance.now());
